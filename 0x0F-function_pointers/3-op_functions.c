#include "3-calc.h"

/**
 * op_add - add function
 * @a: input
 * @b: input
 *
 * Return: int a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract function
 * @a: input
 * @b: input
 *
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply function
 * @a: input
 * @b: input
 *
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide function
 * @a: input
 * @b: input
 *
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo function
 * @a: input
 * @b: input
 *
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
